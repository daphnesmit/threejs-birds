/**
 * Function that adds 2 variables together
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
export function add ( a, b ) {

	return a + b;

}

/**
 * Function that subtracs 2 variables from each other.
 * @param a {number}
 * @param b {number}
 * @returns {number}
 */
export function subtract( a,b ) {

    return a - b;

}
